LCD	EQU	P1
RS	EQU	P2.0
RW	EQU	P2.1
EN	EQU	P2.2

MAIN:
	CLR RW
	LCALL INI
	MOV A,#0C4H
	CALL ESCREVE
	MOV DPTR, #FRASE
	
LOOP:
	MOV A,#0
	MOVC A,@A+DPTR
	CJNE A,#0,ESC
	
TRAVA: 
	JMP TRAVA

ESC:
	CALL DADO
	INC DPTR
	JMP LOOP
	
INI:
	MOV A,#38H
	CALL ESCREVE
	MOV A,#06H
	CALL ESCREVE
	MOV A,#01H
	CALL ESCREVE
	MOV A,#0CH
	CALL ESCREVE
	RET

ESCREVE:
	CLR RS
	CLR EN
	MOV LCD,A
	SETB EN
	CALL DELAY
	CLR EN
	RET

DADO:
	SETB RS
	CLR EN
	MOV LCD,A
	SETB EN
	CALL DELAY
	CLR EN
	RET

DELAY:
    MOV     R7,#0FFH

L1:
    MOV    R6,#0FFH

L2:
    DJNZ    R6,L2
    DJNZ    R7,L1
    RET

FRASE:
	DB 'VINICIUS          N 31 ',0

	END	